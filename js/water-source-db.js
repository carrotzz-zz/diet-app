// 基于水利部《全国重要饮用水水源地名录》的供水来源分类
function classifyWaterSource(cityName, cityInfo, elevationMeters) {
  if (!cityInfo) return null;

  // 先查真实数据
  if (WATER_SOURCE_DB[cityName]) return WATER_SOURCE_DB[cityName];

  // 兜底：按地理特征推测，标注"待核实"
  const { province, region } = cityInfo;
  const e = elevationMeters;

  // 青藏高原 → 地下水/冰川水
  if (['西藏自治区','青海省'].includes(province)) {
    if (e != null && e > 3000) return { type:'冰川融水', label:'冰川雪融水（待核实）', tcm:'高海拔冰川雪融水，寒性大，宜温饮', icon:'🧊' };
    return { type:'地下水型', label:'地下水（待核实）', tcm:'高原地下水，偏硬偏寒，矿物质多', icon:'💧' };
  }

  // 西北干旱省份 → 地下水为主
  if (['新疆维吾尔自治区','宁夏回族自治区','甘肃省','内蒙古自治区'].includes(province)) {
    if (['兰州','银川','呼和浩特','包头'].includes(cityName)) return WATER_SOURCE_DB[cityName] || null;
    return { type:'地下水型', label:'地下水（待核实）', tcm:'西北干旱区，地下水为主，偏硬偏寒，矿物质多', icon:'💧' };
  }

  // 沿海省份 → 以地表水（河道/水库）为主
  if (['福建省','浙江省','广东省','海南省','广西壮族自治区'].includes(province)) {
    return { type:'河道型', label:'地表水（待核实）', tcm:'南方沿海省份，地表水为主，水质偏软', icon:'🌊' };
  }

  // 东北 → 地下水/水库
  if (['黑龙江省','吉林省','辽宁省'].includes(province)) {
    return { type:'混合型', label:'地下水·水库（待核实）', tcm:'东北地区，地下水+水库水源为主', icon:'🏞️' };
  }

  // 华北平原 → 地下水+南水北调
  if (region && region.includes('华北')) {
    return { type:'混合型', label:'地下水·南水北调（待核实）', tcm:'华北平原，地下水+南水北调，水质偏硬', icon:'💧' };
  }

  // 其余：根据区域特征
  if (region && region.includes('湿')) return { type:'河道型', label:'地表水（待核实）', tcm:'湿润地区，地表水为主，水质偏软', icon:'🌊' };
  if (region && region.includes('燥')) return { type:'地下水型', label:'地下水（待核实）', tcm:'干燥地区，地下水为主，偏硬偏寒', icon:'💧' };

  return { type:'待核实', label:'数据待补充', tcm:'暂无该城市的水源数据', icon:'❓' };
}
